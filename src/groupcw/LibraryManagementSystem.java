    private void searchPriceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchPriceActionPerformed
        int filled = 0;
        for (int i = 0; i < libraryTable.getRowCount(); i++) {
            if (libraryTable.getValueAt(i, 0) == null) {
                break;
            }
            filled++;
        }
        Object[] borrowerName = new Object[filled];
        Object[] borrowedBook = new Object[filled];
        Object[] borrowedDate = new Object[filled];
        int[] fine = new int[filled];
        for (int i = 0; i < filled; i++) {
            borrowerName[i] = libraryTable.getValueAt(i, 0);
            borrowedBook[i] = libraryTable.getValueAt(i, 1);
            borrowedDate[i] = libraryTable.getValueAt(i, 2);
            fine[i] = Integer.parseInt(libraryTable.getValueAt(i, 5).toString());
        }
        sort(fine, new Object[][]{borrowedBook, borrowedDate, borrowerName});
        int suitable = binarySearch(fine, Integer.parseInt(search.getText()));
        JOptionPane.showMessageDialog(this, borrowerName[suitable]+"\n"+borrowedBook[suitable]+"\n"+borrowedDate[suitable]+"\n"+fine[suitable]+"\n");
    }//GEN-LAST:event_searchPriceActionPerformed

    private int binarySearch(int[] arr, int target) {
        int s = 0, e = arr.length - 1;
        while (s <= e) {
            int m = s + (e - s) / 2;
            int element = arr[m];
            if (element == target) {
                return m;
            }
            if (element > target) {
                e = m - 1;
            } else {
                s = m + 1;
            }
        }
        return -1;
    }

    private void swap(Object[] arr, int x, int y) {
        Object temp = arr[x];
        arr[x] = arr[y];
        arr[y] = temp;
    }

    private void swap(int[] arr, int x, int y) {
        int temp = arr[x];
        arr[x] = arr[y];
        arr[y] = temp;
    }

    private void sort(int[] arr, Object[][] sortBasedOn) {
        int n = arr.length;
        for (int i = 0; i < n - 1; i++) {
            for (int j = 0; j < n - i - 1; j++) {
                if (arr[j] > arr[j + 1]) {
                    swap(arr, j, j + 1);
                    for (int k = 0; k < sortBasedOn.length; k++) {
                        swap(sortBasedOn[k], j, j + 1);
                    }
                }
            }
        }
    }

