    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        
        for (int i = 0; i < libraryTable.getRowCount(); i++) {
            if (libraryTable.getValueAt(i, 0) != null) {
                continue;
            }
            JTextField[] txt = {nameTF, bookNameTF, borrowedDate, dueDate, returnDate, price};
            for (JTextField t : txt) {
                if (t.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Text field can not be left empty");
                }
            }
            for (int z = 0; z < txt.length; z++) {
                libraryTable.setValueAt(txt[z].getText(), i, z);
            }
            libraryTable.setValueAt(category.getSelectedItem(), i, 6);
            break;
        }
    }//GEN-LAST:event_addActionPerformed

